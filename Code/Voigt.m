function CC = Voigt(DD)
kk = [1,6,5;6,2,4;5,4,3];
CC(6,6) = 0;
for i = 1:3
    for j = 1:3
        m = kk(i,j);
        for k = 1:3
           for l = 1:3
               n = kk(k,l);
               CC(m,n) = DD(i,j,k,l);
           end
        end
    end
end


